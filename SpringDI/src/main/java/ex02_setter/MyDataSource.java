package ex02_setter;

public class MyDataSource {
	private String url;
	private String urd;
	private String upw;
	
	
	//getter, setter
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getUrd() {
		return urd;
	}
	public void setUrd(String urd) {
		this.urd = urd;
	}
	public String getUpw() {
		return upw;
	}
	public void setUpw(String upw) {
		this.upw = upw;
	}
	
	
	
}
